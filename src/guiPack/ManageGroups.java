    private void jButton19ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton19ActionPerformed
// TODO add your handling code here:
        if(jTextGroup.getText().equals("")) {
            new MessageBox(parent, "Invalid Group Name!").setVisible(true);
            
            return;
        }

        SingleGroup sg = new SingleGroup();
        sg.groupName = jTextGroup.getText();
        parent.singleUser.groups.add(sg);
        refreshGroupList();
        refreshRegisteredListFor(null);
        jTextGroup.setText("");
        
    }//GEN-LAST:event_jButton19ActionPerformed

    
