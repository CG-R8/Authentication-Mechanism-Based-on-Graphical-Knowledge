    private void jButton20ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton20ActionPerformed
        // TODO add your handling code here:
        int index = jListAvailableContacts.getSelectedIndex();
        if(index==-1) {
            return;
        }
        
        int groupIndex = jListGroups.getSelectedIndex();
        if(groupIndex==-1) {
            return;
        }
        
        SingleGroup sg = parent.singleUser.groups.get(groupIndex);
        sg.addContact(parent.singleUser.contacts.get(index));
        
        refreshGroupList();
        jListGroups.setSelectedIndex(groupIndex);
        refreshRegisteredListFor(sg);
        
    }//GEN-LAST:event_jButton20ActionPerformed

    private void jButton21ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton21ActionPerformed
        // TODO add your handling code here:
        int index = jListGroups.getSelectedIndex();
        if(index==-1) {
            return;
        }
        MessageBox mb = new MessageBox(parent , "Are You Sure?");
        mb.setVisible(true);
        
        if(!mb.ok) {
            return;
        }
        parent.singleUser.groups.remove(index);
        refreshGroupList();
        refreshRegisteredListFor(null);
        
        
    }//GEN-LAST:event_jButton21ActionPerformed

