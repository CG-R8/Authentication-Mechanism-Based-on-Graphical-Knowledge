    public void refreshGroupList() {
        lmGroups.clear();
        for(SingleGroup sg: parent.singleUser.groups) {
            lmGroups.addElement(sg.groupName);
        }
        jListGroups.setModel(lmGroups);
    }
    
    public void refreshRegisteredListFor(SingleGroup sg) {
        lmContactsRegistered.clear();
        if(sg==null) {
            jListContactsRegistered.setModel(lmContactsRegistered);
            return;
        }
        for(SingleContact sc: sg.contacts) {
            lmContactsRegistered.addElement(sc.name + " " + sc.email);
        }
        jListContactsRegistered.setModel(lmContactsRegistered);
    }
    
    public void fillAvailableUserList() {
        lmContactsAvailable.clear();
        for(SingleContact sc: parent.singleUser.contacts) {
            lmContactsAvailable.addElement(sc.name + " " + sc.email);
        }
        jListAvailableContacts.setModel(lmContactsAvailable);
    }
    
    
