    private void jListRecdMailsValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_jListRecdMailsValueChanged
// TODO add your handling code here:
        int index = jListRecdMails.getSelectedIndex();
        if(index==-1) {
            return;
        }
        SingleMail sm = parent.singleUser.savedMails.get(index);
        jLabelTo.setText("TO: " + sm.to);
        jLabelFrom.setText("FROM: " + sm.from);
        jLabelSubject.setText("SUBJECT: " + sm.subject);
        String dt = sm.dt.get(sm.dt.YEAR) + "-" + (sm.dt.get(sm.dt.MONTH)+1) + "-" + sm.dt.get(sm.dt.DAY_OF_MONTH) + " " + sm.dt.get(sm.dt.HOUR_OF_DAY) + ":" + sm.dt.get(sm.dt.MINUTE);
        jLabelDate.setText("DATE: " + dt);
        
    }//GEN-LAST:event_jListRecdMailsValueChanged

    
