    private void jButton20ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton20ActionPerformed
        // TODO add your handling code here:
        int indices[] = jListRecdMails.getSelectedIndices();
        if(indices.length==0) {
            return;
        }
        MessageBox mb = new MessageBox(parent , "Are You Sure?");
        mb.setVisible(true);
        
        if(!mb.ok) {
            return;
        }
        for(int i=indices.length-1;i>=0;i--) {
            SingleMail smDeleted = parent.singleUser.savedMails.remove(indices[i]);
            for(int j=0;j<smDeleted.attachmentsSavedAs.size();j++) {
                try {
                    new File(smDeleted.attachmentsSavedAs.get(j)).delete();
                }catch(Exception e) {
                    ;
                }
            }
        }
        refreshList();
    }//GEN-LAST:event_jButton20ActionPerformed

    private void jListRecdMailsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jListRecdMailsMouseClicked
        // TODO add your handling code here:
        if(evt.getClickCount()!=2) {
            return;
        }
        int index = jListRecdMails.getSelectedIndex();
        if(index==-1) {
            return;
        }
        SingleMail sm = parent.singleUser.savedMails.get(index);
        new ShowMailContents(parent, sm).setVisible(true);
        
    }//GEN-LAST:event_jListRecdMailsMouseClicked

